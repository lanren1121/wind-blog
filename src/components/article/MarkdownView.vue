<template>
  <div class="markdown-body" v-html="renderedMarkdown"></div>
</template>

<script>
import MarkdownIt from 'markdown-it';

export default {
  name: 'MarkdownView',
  props: {
    content: {
      type: String,
      required: true
    }
  },
  data() {
    return {
      md: null
    };
  },
  computed: {
    renderedMarkdown() {
      if (!this.md) {
        this.md = new MarkdownIt({
          html: true,
          linkify: true,
          typographer: true
        });
      }
      return this.md.render(this.content);
    }
  }
};
</script>

